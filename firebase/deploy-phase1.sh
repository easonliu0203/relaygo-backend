#!/bin/bash
# ============================================================================
# 階段 1: 部署與測試腳本 (Linux/Mac)
# ============================================================================
# 此腳本會依序執行以下步驟：
# 1. 部署 Firestore 安全規則
# 2. 執行資料遷移腳本
# 3. 運行測試用例
# ============================================================================

set -e  # 遇到錯誤立即退出

echo ""
echo "============================================================================"
echo "階段 1: 部署與測試腳本"
echo "============================================================================"
echo ""

# 檢查是否在 firebase 目錄
if [ ! -f "firebase.json" ]; then
    echo "[錯誤] 請在 firebase 目錄中執行此腳本"
    exit 1
fi

# ============================================================================
# 步驟 1: 部署 Firestore 安全規則
# ============================================================================
echo ""
echo "[步驟 1/4] 部署 Firestore 安全規則..."
echo ""

firebase deploy --only firestore:rules

echo ""
echo "[成功] Firestore 安全規則部署完成"
echo ""

# ============================================================================
# 步驟 2: 執行資料遷移腳本
# ============================================================================
echo ""
echo "[步驟 2/4] 執行資料遷移腳本..."
echo ""

# 檢查是否設置了 Service Account Key
if [ ! -f "service-account-key.json" ]; then
    echo "[警告] 未找到 service-account-key.json"
    echo "[提示] 請參考 migrations/README.md 設置 Service Account Key"
    echo ""
    read -p "是否繼續執行遷移腳本？(y/n): " continue
    if [ "$continue" != "y" ]; then
        echo "[取消] 跳過資料遷移"
        skip_migration=true
    fi
fi

if [ "$skip_migration" != "true" ]; then
    echo ""
    echo "[2.1] 遷移用戶語言偏好..."
    node migrations/add-user-language-preferences.js

    echo ""
    echo "[2.2] 遷移聊天室成員列表..."
    node migrations/add-chat-room-member-ids.js

    echo ""
    read -p "[2.3] (可選) 是否執行訊息語言偵測遷移？(y/n): " migrate_messages
    if [ "$migrate_messages" = "y" ]; then
        node migrations/add-message-detected-lang.js || echo "[警告] 訊息語言偵測遷移失敗（可選步驟）"
    fi
fi

echo ""
echo "[成功] 資料遷移完成"
echo ""

# ============================================================================
# 步驟 3: 安裝測試依賴
# ============================================================================
echo ""
echo "[步驟 3/4] 安裝測試依賴..."
echo ""

if [ ! -d "node_modules" ]; then
    echo "[提示] 首次運行，正在安裝依賴..."
    npm install
fi

# ============================================================================
# 步驟 4: 運行測試用例
# ============================================================================
echo ""
echo "[步驟 4/4] 運行測試用例..."
echo ""

echo "[4.1] 運行 Firestore 安全規則測試..."
npm run test:rules || echo "[警告] Firestore 安全規則測試失敗"

echo ""
echo "[4.2] 運行 Flutter 資料模型測試..."
cd ../mobile
flutter test test/models/phase1_data_model_test.dart || echo "[警告] Flutter 資料模型測試失敗"
cd ../firebase

# ============================================================================
# 完成
# ============================================================================
echo ""
echo "============================================================================"
echo "階段 1: 部署與測試完成！"
echo "============================================================================"
echo ""
echo "[總結]"
echo "- Firestore 安全規則已部署"
echo "- 資料遷移已執行"
echo "- 測試用例已運行"
echo ""
echo "[下一步]"
echo "1. 檢查測試結果"
echo "2. 驗證 Firestore 資料"
echo "3. 準備開始階段 2"
echo ""
echo "============================================================================"
echo ""

